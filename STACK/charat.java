import java.util.*;
public class charat {
    
    public static void main(String[] args) {
        String str = "Himanshu ";
        int count =0;
        for(int i=0;i<str.length();i++) {
            System.out.print(str.charAt(i));
            count++;
        }

        System.out.print("//Total letter is " +count);
       
    
}}
